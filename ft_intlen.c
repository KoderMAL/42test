#include "libft.h"

int				ft_intlen(long long int n)
{
	int			len;

	if (n <= LONG_MIN)
		return (20);
	len = 0;
	if (n <= 0)
	{
		len++;
		n = -n;
	}
	while (n > 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
